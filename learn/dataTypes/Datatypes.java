public class Datatypes{
	public static void main(String[] args){
		
		//integer types
		
		byte myByte = 8; // 8 bit signed integer
		short myShort = 9; // 16 bit signed integer
		int myInt = 10;	// 32 bit signed integer
		long myLong = 11111L;		//64 bit signed integer
		
		//floating point numbers
		
		float myFloat = 8.8f;	// 32 bits signed floating number(single precision)
		double myDouble = 9.99;	// 64 - bits signed floating number (double precision)
		
		// other data types

		char myChar = 'V';
		boolean myBoolean = true;

		System.out.println("Byte:" + myByte);
		System.out.println("short:" + myShort);
		System.out.println("int: " + myInt);
		System.out.println("long: " + myLong);
		System.out.println("float : " + myFloat);
		System.out.println("double: " + myDouble);
		System.out.println("char :" + myChar);
		System.out.println("boolean :" + myBoolean);
	}
}